package org.linuxalert.kampuni.model;

public class Item {

  private String name;
  private String subName;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getSubName() {
    return subName;
  }

  public void setSubName(String subName) {
    this.subName = subName;
  }
}
